/*******************************************************************************
* Function Name: DSPinit
********************************************************************************
*
* Summary:
*  Initialize communication
*
* Parameters:
*  None.
*
* Return:
*  None.
*
*******************************************************************************/
void DSPinit()
{
    /* SPI intialisieren */
    SPIinit();
    DSP_reset_Write(0u);
    /* warte auf Start des DSPs*/
    CyDelay(25); 
    /* Umschalten des DSP von I2C auf SPI (3 mal dummy senden) */
    SPIsendNumber((uint8)0x89);
    SPIsendNumber((uint8)0x89);
    SPIsendNumber((uint8)0x89);
    /* Warte auf DSP SPI Initialisierung*/
    CyDelay(25);    
    
    /* Schreiben des EEPROM zu Beginn */
    //uint8 toSend[5];
    uint16 address = 0;
    DSPwrite(address,&e2prom_hex[0],E2PROM_HEX_SIZE);
    
    /*Starte DSP neu*/
    DSP_reset_Write(1u);
    CyDelay(1000);
    DSP_reset_Write(0u);
    
    /*Warte auf Start des DSPs*/
    CyDelay(25); 
    /* Umschalten des DSP von I2C auf SPI (3 mal dummy senden) */
    SPIsendNumber((uint8)0x89AB);
    SPIsendNumber((uint8)0x89AB);
    SPIsendNumber((uint8)0x89AB);
    /* Warte auf DSP SPI Initialisierung*/
    CyDelay(25); 
}

